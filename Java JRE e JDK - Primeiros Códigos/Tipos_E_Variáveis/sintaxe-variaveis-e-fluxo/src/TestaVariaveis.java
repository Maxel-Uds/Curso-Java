public class TestaVariaveis {
	
	public static void main(String[] args) {
		
		System.out.println("Tipos e variáveis");
		
		int idade = 21;
		System.out.println(idade);
		
		idade = 21 + 10;
		System.out.println(idade);
		
		idade = (5 * 7) + 2;
		System.out.println(idade);
		
		System.out.println("A idade é: " + idade);
		
	}
	
}
